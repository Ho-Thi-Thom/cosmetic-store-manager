 public void showTable() {
        int i = 1;
        for (NHACCDTO NhaCcDTO : list) {
            model.addRow(new Object[]{
                i++, NhaCcDTO.getMANCC(), NhaCcDTO.getTENNCC(), NhaCcDTO.getDIACHI(), NhaCcDTO.getEMAIL(), NhaCcDTO.getSDT()
            });
        }
    }

    public void showResult() {
        int i = list.size();
        NHACCDTO NhaCcDTO = list.get(list.size() - 1);
        model.addRow(new Object[]{
            i++, NhaCcDTO.getMANCC(), NhaCcDTO.getTENNCC(), NhaCcDTO.getDIACHI(), NhaCcDTO.getEMAIL(), NhaCcDTO.getSDT()
        });
    }

