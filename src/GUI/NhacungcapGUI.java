    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (kiemtra() == 1) {
            NHACCDTO NhaCcDTO = new NHACCDTO();
            NhaCcDTO.setMANCC(txtMaNCC.getText());
            NhaCcDTO.setTENNCC(txtTenNCC.getText());
            NhaCcDTO.setDIACHI(txtDiaChi.getText());
            NhaCcDTO.setEMAIL(txtEmail.getText());
            NhaCcDTO.setSDT(txtSDT.getText());
            for (NHACCDTO sp : list) {
                if (sp.getMANCC().equals(txtMaNCC.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "Đã tồn tại mã nhà cung cấp");
                    return;
                }
            }
            try {
                if (NhaCcBLL.addNHACCDTO(NhaCcDTO)) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                    list.add(NhaCcDTO);//them vao danh sach
                    showResult();
                    reset();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thêm không thành công ");
                }
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(rootPane, "Thêm không thành công ");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đủ thông tin");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedIndex = tblNhaCc.getSelectedRow();
        if (list.size() == 0) {
            JOptionPane.showMessageDialog(rootPane,
                    "Không có nhà sản xuất!");
        } else if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(rootPane,
                    "Vui lòng chọn nhà cung cấp cần sửa");
        } else if (kiemtra() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đủ thông tin");
        } else {
            NHACCDTO NhaCcDTO = new NHACCDTO();
            NhaCcDTO.setMANCC(txtMaNCC.getText());
            NhaCcDTO.setTENNCC(txtTenNCC.getText());
            NhaCcDTO.setDIACHI(txtDiaChi.getText());
            NhaCcDTO.setEMAIL(txtEmail.getText());
            NhaCcDTO.setSDT(txtSDT.getText());
            if (NhaCcBLL.suaNHACCDTO(NhaCcDTO)) {
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công !");
                model.setRowCount(0);
                list = new NHACCBLL().getALLNHACCDTO();
                showTable();
                reset();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Sửa không thành công ");
            }
        }
        btnThem.setEnabled(true);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int removeIndex = tblNhaCc.getSelectedRow();
        if (removeIndex == -1) {
            JOptionPane.showMessageDialog(rootPane,
                    "Hãy chọn một dòng rồi ấn nút xóa");
        } else if (list.size() == 0) {
            JOptionPane.showMessageDialog(rootPane,
                    "Không có nhà cung cấp nào để xóa");
        } else {
            NHACCDTO NhaCcDTO = new NHACCDTO();
            NhaCcDTO.setMANCC(txtMaNCC.getText());
            if (NhaCcBLL.xoaNHACCDTO(NhaCcDTO)) {
                if (JOptionPane.showConfirmDialog(this, "Xoá nhà cung cấp?", "Confirm",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    model.setRowCount(0);
                    list = new NHACCBLL().getALLNHACCDTO();
                    showTable();
                }
                reset();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Xóa không thành công ");
            }
        }
        btnThem.setEnabled(true);
    }//GEN-LAST:event_btnXoaActionPerformed

