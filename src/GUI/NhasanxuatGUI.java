 public void showTable() {
        int i = 1;
        for (NHASXDTO NhaSxDTO : list) {
            model.addRow(new Object[]{
                i++, NhaSxDTO.getMANSX(), NhaSxDTO.getTENNSX(), NhaSxDTO.getDIACHI(), NhaSxDTO.getEMAIL(), NhaSxDTO.getSDT()
            });
        }
    }

    public void showResult() {
        int i = list.size();
        NHASXDTO NhaSxDTO = list.get(list.size() - 1);
        model.addRow(new Object[]{
            i++, NhaSxDTO.getMANSX(), NhaSxDTO.getTENNSX(), NhaSxDTO.getDIACHI(), NhaSxDTO.getEMAIL(), NhaSxDTO.getSDT()
        });
    }

    public void reset() {
        txtMaNSX.setText("");
        txtTenNSX.setText("");
        txtDiaChi.setText("");
        txtEmail.setText("");
        txtSDT.setText("");
        txtMaNSX.enable(true);
    }

    public int kiemtra() {
        int fag = 1;
        if (txtMaNSX.getText().length() == 0) {
            fag = 0;
        } else if (txtTenNSX.getText().length() == 0) {
            fag = 0;
        } else if (txtDiaChi.getText().length() == 0) {
            fag = 0;
        } else if (txtEmail.getText().length() == 0) {
            fag = 0;
        } else if (txtSDT.getText().length() == 0) {
            fag = 0;
        }
        return fag;
    }

    protected void updateFilter() {
        Object selected = category.getSelectedItem();
        TableRowSorter<TableModel> sorter = (TableRowSorter<TableModel>) tblNhaSx.getRowSorter();
        String text = "(?i)" + txtSearch.getText();
        if ("Nothing".equals(selected)) {
            sorter.setRowFilter(null);
        } else {
            int col = -1;
            if ("Mã nhà sản xuất".equals(selected)) {
                col = 1;
            } else if ("Tên nhà sản xuất".equals(selected)) {
                col = 2;
            } else if ("Địa chỉ".equals(selected)) {
                col = 3;
            } else if ("Email".equals(selected)) {
                col = 4;
            } else if ("SĐT".equals(selected)) {
                col = 5;
            }
            sorter.setRowFilter(RowFilter.regexFilter(text, col));
        }
    }
