    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (kiemtra() == 1) {
            NHASXDTO NhaSxDTO = new NHASXDTO();
            NhaSxDTO.setMANSX(txtMaNSX.getText());
            NhaSxDTO.setTENNSX(txtTenNSX.getText());
            NhaSxDTO.setDIACHI(txtDiaChi.getText());
            NhaSxDTO.setEMAIL(txtEmail.getText());
            NhaSxDTO.setSDT(txtSDT.getText());
            for (NHASXDTO sp : list) {
                if (sp.getMANSX().equals(txtMaNSX.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "Đã tồn tại mã nhà sản xuất");
                    return;
                }
            }
            try {
                if (NhaSxBLL.addNHASXDTO(NhaSxDTO)) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                    list.add(NhaSxDTO);//them vao danh sach
                    showResult();
                    reset();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thêm không thành công ");
                }
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(rootPane, "Thêm không thành công ");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đủ thông tin");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedIndex = tblNhaSx.getSelectedRow();
        if (list.size() == 0) {
            JOptionPane.showMessageDialog(rootPane,
                    "Không có nhà sản xuất!");
        } else if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(rootPane,
                    "Vui lòng chọn nhà sản xuất cần sửa");
        } else if (kiemtra() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đủ thông tin");
        } else {
            NHASXDTO NhaSxDTO = new NHASXDTO();
            NhaSxDTO.setMANSX(txtMaNSX.getText());
            NhaSxDTO.setTENNSX(txtTenNSX.getText());
            NhaSxDTO.setDIACHI(txtDiaChi.getText());
            NhaSxDTO.setEMAIL(txtEmail.getText());
            NhaSxDTO.setSDT(txtSDT.getText());
            if (NhaSxBLL.suaNHASXDTO(NhaSxDTO)) {
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công !");
                model.setRowCount(0);
                list = new NHASXBLL().getALLNHASXDTO();
                showTable();
                reset();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Sửa không thành công ");
            }
        }
        btnThem.setEnabled(true);
    }//GEN-LAST:event_btnSuaActionPerformed

